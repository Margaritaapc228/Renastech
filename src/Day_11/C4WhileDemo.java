package Day_11;

public class C4WhileDemo {
    public static void main(String[] args) {

        for (int n=1; n<=5; n++){
            System.out.println(n);
        }
        System.out.println();

        int j=1;
        while (j<=5){
            System.out.println(j);
            j++;
        }

    }
}
