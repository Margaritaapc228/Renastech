package Day_11;

import java.util.Scanner;

public class C3ScannerDemo {

    public static void main(String[] args) {
        Scanner car = new Scanner(System.in);
        System.out.println("Enter a byte number: ");
        int i = car.nextByte(); System.out.println(i);

        System.out.println("Enter a byte number: ");
        byte b = car.nextByte();
        System.out.println("Enter a byte number is " + b);

        System.out.println("Enter a short number: ");
        short s = car.nextByte();
        System.out.println("Enter a short number is " + s + "=)");

        System.out.println("Enter a long number: ");
        long l = car.nextByte();
        System.out.println("Enter a long number is " + l);

        System.out.println("Enter a float number: ");
        float f = car.nextByte();
        System.out.println("Enter a float number is " + f);






    }


}
