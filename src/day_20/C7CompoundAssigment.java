package day_20;

public class C7CompoundAssigment {
    public static void main(String[] args) {

        int i=10;
        System.out.println(i);
        i=i+10;
        System.out.println(i);

        //+= compound assigment Or augmented assigment
        i+=10; //short hand of i=i+10
        System.out.println(i);
    }
}
