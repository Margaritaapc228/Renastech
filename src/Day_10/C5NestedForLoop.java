package Day_10;

public class C5NestedForLoop {

    /*

    *
    **
    ***
    ****

     */

    public static void main(String[] args) {

        for (int i=1; i<=4; i++){
            for (int x=1; x<=i; x++){

                System.out.print("*");
            }

            System.out.println();
        }

    }
}
